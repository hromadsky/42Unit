#include "libft.h"

int	ft_wordcount(char *s, char d)
{
	unsigned int i;

	i = 0;
	while (*s)
	{
		if (*s != d)
		{
			i++;
			while (*s != d && *s)
			{
				s++;
			}
		}
		else
			s++;
	}
	return (i);
}
